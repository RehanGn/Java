import java.util.Scanner;

public class VowelConsonant {
    public static void main(String[] args) {
        char ch = 'a';
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u'){
            System.out.println("It's Vowel: " + ch);
        }else {
            System.out.println("It's Consonant: " + ch);
        }

    }
}
